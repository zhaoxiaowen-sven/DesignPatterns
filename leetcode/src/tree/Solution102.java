package tree;

public class Solution102 {
    // Definition for a binary tree node.
    public class TreeNode {
        int val;
        Solution094.TreeNode left;
        Solution094.TreeNode right;
        TreeNode() {}
        TreeNode(int val) { this.val = val; }
        TreeNode(int val, Solution094.TreeNode left, Solution094.TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }

}
